        public override string[] GetRolesForUser(string username)
        {
            var db = new Entities();
            var role = (from u in db.Users
                        join r in db.Roles
                            on u.RoleID equals r.RoleID
                        where u.Username == username
                        select r.RoleNameInSystem);
            return role.ToArray();
